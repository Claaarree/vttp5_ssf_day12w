package sg.edu.nus.iss.vttp5a_ssf_day12w.model;

public class Numberr {
    private int name;
    private String url;

    
    public Numberr(int name, String url) {
        this.name = name;
        this.url = url;
    }
    public int getName() {
        return name;
    }
    public void setName(int name) {
        this.name = name;
    }
    public String getUrl() {
        return url;
    }
    public void setUrl(String url) {
        this.url = url;
    }

    
}
